/**
 * CI bootstrap: fvtt.config.js (+ .env for E2E). Release: --release copies example only.
 */
import * as fs from 'node:fs'
import * as path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)))
const releaseOnly = process.argv.includes('--release')

if (releaseOnly) {
  fs.copyFileSync(
    path.join(root, 'fvtt.config.example.js'),
    path.join(root, 'fvtt.config.js')
  )
  console.log('Wrote fvtt.config.js (release bootstrap from example)')
  process.exit(0)
}

const userDataPath = path.resolve(
  process.env.FOUNDRY_USERDATA_HOST || path.join(root, 'foundrydata')
)
fs.mkdirSync(userDataPath, { recursive: true })

const examplePath = path.join(root, 'fvtt.config.example.js')
const exampleModule = await import(examplePath)
const base = { ...exampleModule.default, userDataPath }

const fvttOut = `/** Generated by scripts/write-ci-config.js — do not edit in CI */\nconst developmentOptions = ${JSON.stringify(base, null, 2)}\n\nexport default developmentOptions\n`
fs.writeFileSync(path.join(root, 'fvtt.config.js'), fvttOut, 'utf8')
console.log('Wrote fvtt.config.js userDataPath:', userDataPath)

const username = process.env.FOUNDRY_USERNAME ?? ''
const password = process.env.FOUNDRY_PASSWORD ?? ''
const adminKey = process.env.FOUNDRY_ADMIN_KEY ?? ''
const world = process.env.FOUNDRY_WORLD ?? base.testWorldName ?? 'modern-names-test'

const envLines = [
  '# Generated by scripts/write-ci-config.js',
  `FOUNDRY_USERDATA_HOST=${userDataPath}`,
  `FOUNDRY_WORLD=${world}`,
  `FOUNDRY_USERNAME=${username}`,
  `FOUNDRY_PASSWORD=${password}`,
  `FOUNDRY_ADMIN_KEY=${adminKey}`,
  ''
]
fs.writeFileSync(path.join(root, '.env'), envLines.join('\n'), 'utf8')
console.log('Wrote .env for Docker / Cypress')

const dockerSecret = path.join(root, 'docker', 'secret')
fs.mkdirSync(dockerSecret, { recursive: true })
